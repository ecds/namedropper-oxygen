package edu.emory.library.namedropper.plugins;

/*
 * Class that contaions a single result choice generated byt autoSuggest query
 */
public class ResultChoice {
    private String term;
    private String viafid;

    // Default constructor
    public ResultChoice(){}

    /*
     * Constructor that creates ResultChoice object and populates viafid and term
     * @param    String viafid
     * @param  String term
     */
    public ResultChoice(String viafid, String term){
        this.term = term;
        this.viafid = viafid;
    }

    // Access functions
    public String getViafid(){return this.viafid;}
    public String getTerm(){return this.term;}

    public void settViafid(String viafid){this.viafid = viafid;}
    public void setTerm(String term){this.term = term;}

    // String Rep.
    public String toString() {return this.term;}
}